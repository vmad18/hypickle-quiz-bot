package mod.quizbot.me.utils.mathstuff;

public class TrigFunction {

    public String sin(){ return "sin(x)";}

    public String cos(){return "cos(x)";}

    public String tan(){return "tan(x)";}

    public String csc(){return "1/sin(x)";}

    public String sec(){return "1/cos(x)";}

    public String cot(){return "cos(x)/sin(x)";}

    public String dcsc(){return "-1*cos(x)^2/sin(x)^2";}

    public String dsec(){return "-1*sin(x)^2/cos(x)^2";}
}
